    <p>
      Dengue, a mosquito-borne disease emerged in Nepal in the form of Dengue
      Fever (DF), Dengue Hemorrhagic Fever (DHF) and Dengue Shock Syndrome
      (DSS). The earliest cases were detected as early as 2005. The sporadic
      cases continued and outbreaks occurred in 2006 and 2010. Initially most of
      the reported cases had travel history to neighbouring country (India),
      however lately indigenous cases were also reported. The affected districts
      were Chitwan, Kanchanpur, Kailali, Banke, Bardiya, Dang, Kapilvastu,
      Parsa, Rupandehi, Rautahat, Sarlahi, Saptari and Jhapa, indicating spread
      throughout the country from west to east lying in the plain Terai region.
      During 2011, 79 confirmed cases were reported from 15 districts with the
      highest case incidence in Chitwan (n=55) Aedes aegipti (mosquito-vector)
      has been identified in 5 peri-urban areas of Terai region (Kailali, Dang,
      Chitwan, Parsa and Jhapa) during entomological surveillance conducted by
      EDCD during the year 2006-2010, indicating local transmission of dengue.
      Studies carried out in close collaboration with WARUN/AFRIMS in the year
      2006 by EDCD/NPHL showed all 4 sub-types (DEN-1, DEN-2, DEN-3 and DEN-4)
      of Dengue virus circulation in Nepal.
    </p>

