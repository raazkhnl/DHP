    <p>
      Malaria control project in Nepal was first initiated in 1954 with the
      support from USAID (the then USOM), with the objective of controlling
      malaria, mainly in plain region (Terai belt) of central Nepal. In 1958,
      national malaria eradication program was launched with the objective of
      eradicating malaria from the country. Due to various reasons the
      eradication concept reverted to control program in 1978. Following the
      call of WHO to revamp the malaria control programs in 1998, Roll Back
      Malaria (RBM) initiative was launched to control malaria transmission in
      hard-core forests, foot-hills, inner-Terai and hill river valleys, which
      accounted for more than 70% of the total malaria cases in the country. The
      high risk of acquiring the disease is attributed to the abundance of
      vector mosquitoes, mobile and vulnerable population, relative
      inaccessibility of the area, suitable temperature, environmental and
      socio-economic factors. The recent micro-stratification, based on number
      of malaria cases, geo-ecology, vector breeding and vulnerability due to
      migration and population movements, further narrowed down the essentials
      to VDC level risks as compared to earlier district as units of risks. It
      has clearly shown that only 54 VDC’s are at high risk, 201 VDC’s at
      moderate risk and 999 VDC’s at low risk. Based on the micro-stratification
      report 2013, approximately 13.02 million population (47.9%) live in
      malaria endemic areas (VDCS); out of which ~1 million (3.62%) live in high
      risk VDCs, 2.66 million (9.8%) live in moderate risk VDCs, and 9.38
      million(34.52%) live in low risk VDCs. A total population of 14.13 (52.1%)
      is estimated to live in VDCs where there is no malaria transmission. The
      high risk areas consist of foothills with river belts, forest fringe areas
      in terai, hill river valleys, and inner terai areas. Low risk VDCs lie in
      plain cultivated outer Terai, mountain, and valleys in the mountains.
    </p>

    <div style="color: brown"><h1 class="mt-3">Kala-azar Elimination Programme</h1></div>
    <hr />

    <!-- <h3 class="text-center" style="color: black">
      <u>Background and Scope</u>
    </h3> -->

    <p>
      Kala-azar is a vector-borne disease caused by parasite Leishmania
      donovani, transmitted by the sand fly, Phlebotomusargentipes. The disease
      is characterized by fever for more than two weeks with spleenomegaly,
      anaemia, and progressive weight loss and sometimes darkening of the skin.
      In the endemic areas, children and young adults are its principal victims.
      The disease is fatal if it is not treated timely. Kala-azar and HIV/TB
      co-infections have emerged as a health problem in recent years. The
      government of Nepal has committed to the regional strategy to eliminate
      Kala-azar, and is a signatory (along with India and Bangladesh) of the
      memorandum of understanding that was formalized during the World Health
      Assembly 2005 on Kala-azar elimination. The target of this memorandum is
      achieving Kala-azar disease elimination by 2015. In 2005, Epidemiology and
      Disease Control Division (EDCD) of Department of Health Services also
      formulated a National Plan for the Elimination of Kala-azar, which is
      divided into three phases: Preparatory Phase: 2005-2008 Attack Phase:
      2008-2015 Consolidation Phase: 2015 onwards. The target is to reduce the
      annual incidence of Kala-azar to less than 1 per 10,000 populations at the
      district level by 2015. Over the last decade, there have been some
      significant advances both in the diagnosis and treatment of Kala-azar. The
      rK39 dipstick test kit, a rapid and easily applicable serological test has
      been widely used in the Indian subcontinent including Nepal. The national
      programme to eliminate KA in Nepal has made this test kit available up to
      PHC level of programme districts.
    </p>


