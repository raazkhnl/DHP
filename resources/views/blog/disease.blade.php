    <p>
      The six zoonoses like Taeniosis/cysticercosis /Neurocysticercosis,
      Leptospirosis, Hydatidosis, Brucellosis; Toxoplasmosis and Avian Influenza
      have been identified as Priorities Zoonotic Diseases in Nepal as they are
      found to have epidemic potential. More than 35000 people take
      post-exposure treatment in suspicious or rabid animal bite per annum and
      about 100 people die annually due to rabies. Likewise, snake-bite is a key
      medical emergency and cause of hospital admission. It results in the death
      or chronic disability of many active young people, especially those
      involved in farming and plantation. Number of case of dog and snake bites
      is increasing in the country but there is a decreasing trend of death. The
      Department of Health Services, Epidemiology and Disease Control Division
      (EDCD) responds timely to reduce human sufferings and deaths due to
      poisonous snake bites, control and prevent rabies and other zoonosis in
      coordination and collaboration with public, non-governmental and private
      partners.
    </p>

    <div style="color: brown">
      <h1 class="mt-3">Leprosy Control Programme</h1>
    </div>
    <hr />

    <!-- <h3 class="text-center" style="color: black">
            <u>Background and Scope</u>
          </h3> -->

    <p>
      Nepal has achieved the elimination of leprosy as a public health problem
      in December 2009 and declared elimination in 2010. Department of Health
      Services acknowledged the achievement as one of the major success stories
      of the health sector in the past decades. Although significant progress
      has been made in reducing the disease burden at national level, sustaining
      the achievement & further reducing the disease burden through delivering
      quality leprosy services still remain as major challenge. After meeting
      the elimination target at national level, the national strategy was
      revised to “Sustain Quality Leprosy Services and Further Reduce the
      Disease Burden due to Leprosy in Nepal: 2011-2015″ based on the “Enhanced
      Global Strategy for Further Reducing the Disease Burden Due to Leprosy:
      2011-2015″ and the updated national Operational Guideline is developed
      based upon WHO guideline. The main principles of leprosy control are based
      on early detection of new cases and timely treatment with multi-drug
      therapy (MDT) through integrated health services. Major target for 2020 is
      to reduce grade II disability below 1 per million.
    </p>

