    <p>
      Tuberculosis (TB) remains as a public health problem in Nepal, as it is
      responsible for ill health among thousands of people each year. Nepal aims
      to end tuberculosis epidemic by 2050 with the intermediate target of
      reducing TB incidence by 20% by the year 2021 compared to 2015 and
      increase case notifications by a cumulative total of 20,000 from July 2016
      to July 2021.
    </p>

    <div style="color: brown">
      <h1 class="mt-3">HIV and STI Control Programme</h1>
    </div>
    <hr />

    <!-- <h3 class="text-center" style="color: black">
                <u>Background and Scope</u>
              </h3> -->

    <p>
      Pursuant to its goal of achieving universal access to prevention,
      treatment care and support, HIV Testing Services (HTS) has been a
      strategic focus in the national response to HIV ever since Nepal started
      its response to HIV. The first ever HTC began in 1995 with the approach of
      voluntary Client-Initiated Testing and Counseling (CITC). Moving further
      from its previous approach of voluntary CITC, the national HIV testing and
      counseling program has been later widened to include Provider-Initiated
      Testing and Counseling (PITC), as well as CITC as crucial components of
      the nation’s fight against HIV. With the expansion of HTC sites across the
      country, there has been parallel development. National Guidelines on HTC
      was formulated in 2003 and updated in 2007, 2009 and 2011 and later on the
      separate guideline is merged as comprehensive guideline on treating and
      preventing HIV in 2014, community-based HIV testing is recommended to
      cover HIV screening widerA new consolidated guideline on treating and
      preventing HIV is being endorsed and will be finalized by the end of
      December 2016, which would recommend for community led testing. Human
      resources for HTC have been trained for public health facilities as well
      as NGOs-run HTS sites. A new National HIV Strategic Plan 2016-2021 is
      recently launched to achieve ambitious global goals of 90-90-90. By 2020,
      90% of all people living with HIV will know their HIV status by 2020, 90%
      of all people with diagnosed HIV infection will receive sustained
      antiretroviral therapy andby 2020, 90% of all people receiving
      antiretroviral therapy will have viral suppression. HIV and STI
      counselling and testing is available up to the health post level and
      through the stand alone HTC sites run by the NGOs.
    </p>
  
