      <h1>Adolescent and Sexual Reproductive Health Programme</h1>
    </div>
    <hr />


    <p>
      The overall goal of the National ASRH Program is to promote the sexual and
      reproductive health status of adolescents. The major target of the program
      was to make 1,000 health facilities adolescent‐friendly by 2015. The NHSS
      has envisioned that all the services provided through the public health
      facilities will be friendly in all the districts. . Through improving
      access to family planning information and devices, the program aims to
      reduce the Adolescent Fertility Rate (AFR) which is the outcome indicator
      of NHSS. As of 2016, 1534 health facilities are already adolescent
      friendly.
    </p>

    <div style="color: brown">
      <h1>Primary Health Care Outreach Progamme</h1>
    </div>
    <hr />


    <p>
      As envisaged in the National Health Policy 1991, health facilities were
      extended up to village level. However, utilization of services provided by
      health facilities, especially preventive and promotive services, has been
      found to be limited because of limited accessibility. Therefore it was
      felt that services should be expanded closer to the community. Thus
      Primary Health Care Outreach (PHC/ORC) services was initiated and
      established in 1994 (2051 BS). The aim of PHC/ORC is to improve access to
      some basic health services including family planning, child health and
      safe motherhood closer to rural households. These clinics are the service
      extension sites of PHCCs, HPs and up to community level.
    </p>

