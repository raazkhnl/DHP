    <div style="color: brown"><h1>Female Community Health Programme</h1></div>
    <hr />

   
    <p>
      Recognizing the importance of women's participation in promoting health of
      the people, GoN initiated the Female Community Health Volunteer (FCHV)
      Program in FY 2045/46 (1988/1989) in 27 districts and expanded to all 75
      districts of the country in a phased manner. Initially, the approach was
      to select one FCHV per ward regardless of the population size. Later in
      2050 (1993/94) population based approach was introduced in selected (28)
      districts. All together there are 51,416 FCHVs in the country (46,088
      FCHVs at rural/VDC level and 5,328 at urban/municipality level). FCHVs are
      selected by members of Mothers' Group for Health (MG‐H) with the help of
      local health facility staff. They are provided 18 days basic training in
      two phases (9+9 days) on selected primary health care components. The
      major role of the FCHV is to advocate healthy behaviors of mothers and
      community people to promote safe motherhood, child health, family
      planning, and other community based health promotion and service delivery.
      FCHVs distribute condoms and pills, ORS packets and vitamin A capsules,
      treat pneumonia cases and refer and serious cases to health institution
      along with motivation and education to community people. Similarly, they
      also distribute iron tablets to pregnant women. The FCHV program strategy
      has been revised in 2067 (2010) which gave strategic directions and
      critical approaches to ensure a strengthened national program for
      consistency and continuous support to each FCHV. Government of Nepal (GoN)
      is committed to increase the moral & participation of FCHV in community
      health development. In fiscal year 2064/65 MoH established FCHVs fund by
      providing cash support of Rs. 50,000 to each VDC. The mobilization of this
      fund for income generation activities is expected to benefit the FCHVs and
      the community at large.
    </p>

