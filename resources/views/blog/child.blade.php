    <p>
      National Immunization Programme (NIP) is the priority 1 (P1) program of
      Nepal. Launched as “Expanded Program on Immunization” in 2034 BS, national
      Immunization program has met several milestones during the past decades,
      including reduction of under 5 mortality goal (MDG 4). National
      immunization program provides equitable services to the geographically,
      economically hard to reach, marginalized community through more than
      16,000 outreach sessions. Currently, 11 antigens are provided through
      national immunization program. The new initiatives of "Reaching Every
      Child" through declaring country as Fully Immunized initiated in 2012 has
      gained its height through community involvement and as of the date 18
      districts and more than 1800 VDCs have been declared as fully immunized.
      Nepal is polio free since 2010 and has switched to bOPV from tOPV since
      April 17 2016 (Baisakh 5 2073). Maternal and neonatal tetanus elimination
      status has been sustained since 2005. The burden of Japanese encephalitis
      has been successfully reduced during the reporting period. The progress
      towards the measles elimination and control of rubella /congenital rubella
      syndrome by 2019 is in progress. A new guideline on measles case based
      surveillance and Rubella/CRS control has been drafted and underway to
      endorsement.
    </p>

