    <p>
      Nutrition interventions have been proved as one of the most cost effective
      investments for overall socio-economic development by enhancing human
      capital through improved productivity of the population. Hence,
      nutritional well-being of the population is crucial for accelerated
      attainment of many of the Sustainable Development Goals. In an alignment
      with several international and national declarations and National Health
      Policies including International Conference on Nutrition, Government of
      Nepal is committed to its citizens for adequate food, health and nutrition
      to maintain their nutritional well-being. The Constitution has also
      ensured the right to food, health and nutrition to all citizens in Nepal.
      Failing to ensure protection of the population from the hunger and
      under-nutrition often results in the vicious cycle of malnutrition and
      infections leading to poor cognitive and intellectual development
      adversely affecting which, in turn, lead to less productivity leading to
      compromised socio-economic development. When combined with household food
      insecurity, frequent illness and infections, inadequate dietary intake,
      poor hygiene, care and practices, the cycle of inter-generational
      malnutrition comes into existence and continues.
    </p>


  
